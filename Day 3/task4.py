# Dictionaries 
# Store student info in a dictionary and print it

student = {
    "name": "Kifal",
    "age": "20",
    "is_student": True,
    "courses": ["Math", "Odoo", "Python"]

}
print("Student Info:", student) # Printing the whole dictionary
print("Name:", student["name"]) # Accessing a value by key
print("Courses:", student["courses"]) # Accessing a list value by key
print("Is Student:", student["is_student"]) # Accessing a boolean value by key