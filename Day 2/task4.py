# While LOOP 
# print numbers from 1 to 10 using while loop
count = 1
while count <= 10:
    print(count)
    count += 1 # this is same as count = count + 1
# if we don't increment the count variable the loop will run forever
# so be careful while using while loop
# you can also print in reverse order
count = 5
while count >= -10:
    print(count)
    count -= 1 # this is same as count = count - 1
