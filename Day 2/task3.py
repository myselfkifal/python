# For Loop
# print numbers from 1 to 10
for i in range(1, 11): # why 11? because range is exclusive of the last number in easy words it will print from 1 to 10
    print(i) 

    # you can use any variable name in place of i like j,k,num etc
# but i is mostly used as a convention
# you can also print in reverse order
for i in range(10, 0, -1): # why -1? because we want to decrement the value
    print(i)
    